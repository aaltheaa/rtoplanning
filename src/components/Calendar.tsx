  const getNextStatus = useCallback((day: number): AttendanceStatus => {
    const key = dateKeyFromParts(viewYear, viewMonth, day)
    const currentStatus = dayStatus[key] || 'not-set'
    const currentIndex = STATUS_CYCLE.indexOf(currentStatus)
    return STATUS_CYCLE[(currentIndex + 1) % STATUS_CYCLE.length]
  }, [viewYear, viewMonth, dayStatus])

  // Handle drag start (mouse or touch)
  const handleDragStart = useCallback((day: number) => {
    if (isSettingStartWeek) {
      setStartWeekDate(new Date(viewYear, viewMonth, day))
      setIsSettingStartWeek(false)
      return
    }

    const nextStatus = getNextStatus(day)
    setIsDragging(true)
    setDragStatus(nextStatus)
    applyStatusToDay(day, nextStatus)
  }, [isSettingStartWeek, viewYear, viewMonth, getNextStatus, applyStatusToDay])

  // Handle drag move (apply status to day under cursor/touch)
  const handleDragMove = useCallback((day: number) => {
    if (isDragging && dragStatus !== null) {
      applyStatusToDay(day, dragStatus)
    }
  }, [isDragging, dragStatus, applyStatusToDay])

  // Handle drag end
  const handleDragEnd = useCallback(() => {
    setIsDragging(false)
    setDragStatus(null)
  }, [])

  // Global mouse/touch up listener to end drag
  useEffect(() => {
    const handleGlobalUp = () => {
      if (isDragging) {
        handleDragEnd()
      }
    }

    window.addEventListener('mouseup', handleGlobalUp)
    window.addEventListener('touchend', handleGlobalUp)

    return () => {
      window.removeEventListener('mouseup', handleGlobalUp)
      window.removeEventListener('touchend', handleGlobalUp)
    }
  }, [isDragging, handleDragEnd])

