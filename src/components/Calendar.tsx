  const calendarRef = useRef<HTMLDivElement>(null)

  // Generate auto-vacations from OOF days and combine with manual vacations
  const manualVacations = vacations.filter(v => !v.isAutoGenerated)
  const autoVacations = generateAutoVacations(dayStatus, manualVacations)
  const allVacations = [...manualVacations, ...autoVacations].sort((a, b) =>
    a.startDate.localeCompare(b.startDate)
  )

  const daysInMonth = getDaysInMonth(viewYear, viewMonth)
  const firstDay = getFirstDayOfMonth(viewYear, viewMonth)
  const monthName = new Date(viewYear, viewMonth).toLocaleString('default', { month: 'long' })

  const prevMonth = () => {
    if (viewMonth === 0) {
      setViewMonth(11)
      setViewYear(viewYear - 1)
    } else {
      setViewMonth(viewMonth - 1)
    }
  }

  const nextMonth = () => {
    if (viewMonth === 11) {
      setViewMonth(0)
      setViewYear(viewYear + 1)
    } else {
      setViewMonth(viewMonth + 1)
    }
  }

